def tr(key, lang="en"):
    """Get translation for key in specified language with fallback."""
    # 1. Get Target Dict
    lang_code = LANGUAGES.get(lang, "en")
    if lang in TRANSLATIONS: lang_code = lang
    target_dict = TRANSLATIONS.get(lang_code, TRANSLATIONS["en"])
    
    # 2. Lookup
    if key in target_dict:
        return target_dict[key]
    
    # 3. Fallback to English
    return TRANSLATIONS["en"].get(key, key)

